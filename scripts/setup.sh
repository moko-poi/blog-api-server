#!/bin/bash

# Blog API Server Development Setup Script
# This script sets up the local development environment

set -e  # Exit on any error

echo "ğŸš€ Setting up Blog API Server development environment..."

# Check if Go is installed
if ! command -v go &> /dev/null; then
    echo "âŒ Go is not installed. Please install Go 1.24 or later."
    echo "   Visit: https://golang.org/dl/"
    exit 1
fi

# Check Go version
GO_VERSION=$(go version | cut -d' ' -f3 | sed 's/go//')
REQUIRED_VERSION="1.24"
if [ "$(printf '%s\n' "$REQUIRED_VERSION" "$GO_VERSION" | sort -V | head -n1)" != "$REQUIRED_VERSION" ]; then
    echo "âŒ Go version $GO_VERSION is too old. Please upgrade to Go $REQUIRED_VERSION or later."
    exit 1
fi

echo "âœ… Go version $GO_VERSION is installed"

# Check if Docker is installed (optional)
if command -v docker &> /dev/null; then
    echo "âœ… Docker is installed"
    DOCKER_AVAILABLE=true
else
    echo "âš ï¸  Docker is not installed (optional for development)"
    DOCKER_AVAILABLE=false
fi

# Create .env file from example if it doesn't exist
if [ ! -f .env ]; then
    if [ -f .env.example ]; then
        cp .env.example .env
        echo "âœ… Created .env file from .env.example"
    else
        echo "âš ï¸  .env.example not found, creating basic .env file"
        cat > .env << EOF
HOST=localhost
PORT=8080
LOG_LEVEL=debug
READ_TIMEOUT=10s
WRITE_TIMEOUT=10s
IDLE_TIMEOUT=120s
DEV_MODE=true
EOF
    fi
else
    echo "âœ… .env file already exists"
fi

# Install Go dependencies
echo "ğŸ“¦ Installing Go dependencies..."
go mod download
go mod tidy

# Install development tools
echo "ğŸ”§ Installing development tools..."

# Install Air for hot reloading (optional)
if command -v air &> /dev/null; then
    echo "âœ… Air is already installed"
else
    echo "ğŸ“¦ Installing Air for hot reloading..."
    go install github.com/air-verse/air@latest
    if [ $? -eq 0 ]; then
        echo "âœ… Air installed successfully"
    else
        echo "âš ï¸  Failed to install Air. Hot reloading may not work."
    fi
fi

# Create tmp directory for Air
mkdir -p tmp

# Test build
echo "ğŸ”¨ Testing build..."
if go build -o ./tmp/test-build ./cmd/server; then
    echo "âœ… Build successful"
    rm -f ./tmp/test-build
else
    echo "âŒ Build failed"
    exit 1
fi

# Run tests
echo "ğŸ§ª Running tests..."
if go test ./...; then
    echo "âœ… All tests passed"
else
    echo "âŒ Some tests failed"
    exit 1
fi

echo ""
echo "ğŸ‰ Development environment setup complete!"
echo ""
echo "ğŸ“‹ Next steps:"
echo "   â€¢ Run 'make dev' to start the development server with hot reload"
echo "   â€¢ Run 'make test' to run all tests"
echo "   â€¢ Run 'make audit' for code quality checks"
if [ "$DOCKER_AVAILABLE" = true ]; then
