fi
echo "   â€¢ Visit http://localhost:8080/healthz to check if the server is running"
echo ""
echo "ğŸ“– Available commands:"
echo "   â€¢ make help - Show all available make targets"
echo "   â€¢ make build - Build production binary"
echo "   â€¢ make test-cover - Run tests with coverage report"